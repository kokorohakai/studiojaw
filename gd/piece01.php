<h2>Strategy:</h2>
<b>Purpose:</b><br>
The task was to create a poster that created awareness, promoted “going green,” and directed users to visit the goinggreen.com
web-site.<br>
<br>
<b>Intended Audience:</b><br>
The poster was meant to appeal to fellow artists, graphic designers, and anyone dealing with print. That age group is assumed to be
18–30.<br>
<br>
<b>Intended Emotion:</b><br>
I felt the emotion they were looking for was something to make the audience feel guilty for their “un-green” choices, as most
“making aware” posters do.<br>
<br>
<h2>Concept:</h2>
<b>Emotion:</b><br>
Trusting, inviting, innocent, but slightly shaming—making the audience feel bad for not being “greener” in their everyday life. I
chose a young girl to create the innocent feeling, but I also wanted it to appear she really cared for the planet as if she were “Gaia”
(Terra).<br>
<br>
<b>Font:</b><br>
The font chosen is “Gaelic”, which is a Gaelic font. I chose this because when I think of green, I think Ireland. This font also has a
very Art Nouveau feel to it that adds to the earthy feel I’m trying overall to give the piece.<br>
<br>
<b>Color:</b><br>
It was somewhat obvious green should be chosen, but to set the mood of trusting and add a general earthy feeling to the piece, I
chose to use blue as well. Terra’s hair was purposefully chosen to be green, to fit the motif of the piece in general.<br>
<br>
<b>Shape:</b><br>
Organic shapes were chosen to give a more earthy feeling.<br>
<br>
<b>Emphasis/Flow:</b><br>
Emphasis starts with the slogan, then moves down towards her face, then your eyes will land where her arms and legs are
enclosing the globe she’s holding in her lap.<br>
<br>
<b>Other Design Choices:</b><br>
I chose manga for the illustration style to create a more innocent looking character. The leaves were added around the type to
add a more organic feel to the piece. As well as the leaves in the background, the “Learn more” was contrasted to stand out as a
secondary emphasis. This is something a bit complicated to explain, but the idea was to stand out on its own in case you’ve seen
the poster and all you wanted to know was, “What’s that website again?”<br>
<br>
<h2>Research:</h2>
I looked into what kind of imagery the goinggreen.com website used. I also looked into what kind of color scheme they used and
what their general message is. That is how I chose to go with the, “We only have one” slogan. I used How to Draw Manga books
to help refine my drawing ability for this piece. I also researched other websites and looked over other “green” themed posters to
see how they developed their concept.<br>
<br>
<h2>Development:</h2>
The globe was created by using Google maps and vector tracing the world. Then the end product was colorized and morphed into
the globe shape you now see. Terra was hand drawn by me, using various manga images as reference and How to Draw Manga
books. I then vectorized Terra using Illustrator. The logo was created by first vector laying out the font, then bringing it into
illustrator and using a brush that I created with vector leaves to outline the font. The outline was done using a touchscreen monitor.
The background was done in illustrator using vector art and brush strokes.<br>
<br>
<h2>Execution:</h2>
Where this version of the poster is much improved, I was satisfied before this version of the poster. Currently, I use the previous
version in my studio. I will be reprinting this poster for my studio. I would like to see this poster used in other places, and I
generally get a warm reception from friends who have seen this poster. It is something I really enjoyed creating and feel strongly
meets the criteria needed.<br>
