<h2>Strategy:</h2>
<b>Purpose:</b><br>
To create a Shakespeare theme energy drink.<br>
<br>
<b>Intended Audience:</b><br>
Teens and young adults, ranging from age 13–30.<br>
<br>
<b>Intended Emotion:</b><br>
Edgy, exciting, extreme.<br>
<br>
<h2>Concept:</h2>
<b>Actual Emotion:</b><br>
There were going to be two products: “Laughing Juice” and “Tragedy Juice.” I only had time for the one. Laughing Juice needed a
kind of happy feel to it, so I went with yellow for the primary color.<br>
<br>
<b>Fonts:</b><br>
For the body content, I wanted something that was nice, clean, and readable, so I chose Tahoma. The nutrition facts label I wanted
to look industry standard, so I chose Helvetica. For the logo/front label, I wanted a font that was playful and expressed the playful
nature of the product, so I chose “First Grader”.<br>
<br>
<b>Color:</b><br>
Yellow was chosen for the label to be a fun energetic color, and blue was chosen for some of the typography to balance it between
the black and yellow.<br>
<br>
<b>Shape/texture:</b><br>
To make the product feel more “Shakespearean,” I wanted shapes that were organic. I chose a rough texture to give it kind of an
aged feel.<br>
<br>
<b>Emphasis/Flow:</b><br>
Starting with the laughing face, the product then leads you to its name. Then it intrigues you to turn it to its side and read the
description.<br>
<br>
<b>Other Design Choices:</b><br>
I chose to do the outside trim the way I did, because I wanted to have the old-fashioned feel to it yet remain edgy at the same time.
I figured giving it a trim is pretty old fashioned, but taking that trim and making it edgy would make it feel more like an energy
drink.<br>
<br>
<h2>Research:</h2>
I researched various different Shakespeare plays and stories. I read over synopses and created a few thumbnails based on what I
saw. During the process I started to wonder about the masks I usually see associated with theatre. So, I began to research that. I
then found out that these masks were conceived in the time Shakespeare’s plays were put on all across Europe. I then found out
that they were directly associated with the moods of his plays. I then decided to go with the Comedy/Tragedy idea together. After
refinement, I decided it may be a good idea to do one of each.<br>
<br>
<h2>Development:</h2>
The face was hand drawn then vectored in Illustrator. The background was a crumpled sheet of paper that was covered in cola and
then enhanced and colorized in Photoshop. The skull (look closely to the nutrition label) was designed in Illustrator. The barcode is
an actual barcode, a kind of a signature that will actually scan as my name. The typography was added over the layout in InDesign.<br>
<br>
<h2>Execution:</h2>
This piece was featured in the March 2011 Art Gallery at KVCC. It is a very simple and functioning label and could be mass
produced as economically as any other label. The cutting process will need a special die cut, but if the product is mass produced,
the die shouldn’t be anything to worry about.<br>
