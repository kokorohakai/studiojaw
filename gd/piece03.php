<h2>Strategy:</h2>
<b>Purpose:</b><br>
To promote an upcoming seminar focusing on teaching shakespeare in schools, and get students actively involved in the promotion.<br>
<br>
<b>Intended Audience:</b><br>
Teens and middle school students.<br>
<br>
<b>Intended Emotion:</b><br>
Fun and interactive.<br>
<br>
<h2>Concept:</h2>
<b>Actual Emotion:</b><br>
Elegent, interactive and fun.<br>
<br>
<b>Color:</b><br>
warm colors were used for Juliet to create a color harmony / balance between the "Sky/cloud" pattern behind her. Colors that were more
bright and vivid were chosen to make the piece more fun and exciting.<br>
<br>
<b>Font:</b><br>
The font used in her hair was chosen to give the piece a feeling of sophistication, as well as the modern sharp 
font used for the seminar information.<br>
<br>
<b>other Design Choices:</b><br>
The idea is each color would be printed to a transparency for an overhead projector and distrubuted to the schools. The students would 
then be given the buckets of paint for each color to be painted on a school wall (Assuming the school has the commonly seen "Brick wall 
painted white hallway" somewhere. We would then supply white paint for students to paint over the advertisement when the event was over.
The design had to be simple enough to be painted by the students, but at the same time, a fun and attractive design that the students 
would love to paint. The concept was to cause the students to be interactive at creating the advertisement, causing the students to 
be more enthusiastic about the event, and passing that enthusiasm along to other students.<br>
<br>
<h2>Research:</h2>
A lot of research was done using this site:<br>
<a href="http://www.shakespearesociety.org/shakespeare-in-schools.html">http://www.shakespearesociety.org/shakespeare-in-schools.html</a>
Before making a piece, I needed to fully understand what this organization was and what they did. I then took into consideration the audience
and considered the best way to get an group like that to be interested in something.<br>
<br>
This also took a bit of understand shakespeare and his plays, as the last piece "Shakespeare Energy Drink". So I was able to use a lot of 
the research from that piece on this one.<br>
<br>
<h2>Development:</h2>
A photo was taken of a fellow student, then I Vector traced her contour in illustrator by hand. Then end 
piece was created all in illustrator and carefully seperated to be printed in different layers.<br>
<br>
<h2>Execution:</h2>
As mentioned above, if used, middle schools would be sent the transparencies for overhead projects, and 
sent the buckets of paint required for the ad. The school would have either a drama department, music class, or 
some kind of related group in the school, paint the advertisement. This would need to be supplemented
with something simple to send to parents as well, I imaging the end advertisement printed in black and white. 
They would even write in their own date and time for the event to be held at their school, so it can be reused. 
The cost should be relatively low.