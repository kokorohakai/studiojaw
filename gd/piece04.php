<h2>Strategy:</h2>
<b>Purpose:</b><br>
To promote an upcoming event named “Art Stomp” and to provide a functioning map of the locations of viewing sites.<br>
<br>
<b>Intended Audience:</b><br>
Anyone interested in art of all ages including teens, young adults, and parents.<br>
<br>
<b>Intended Emotion:</b><br>
Fun and exciting.<br>
<br>
<h2>Concept:</h2>
<b>Actual Emotion:</b><br>
Fun and exciting emotions were produced here by color, perspective, and movement.<br>
<br>
<b>Line:</b><br>
The line of the artwork uses a lot of diagonals to make the card more exciting to look at.<br>
<br>
<b>Perspective:</b><br>
The perspective used on the artwork was extremely close up, again to create an exciting look and create unification of the piece.<br>
<br>
<b>Fonts:</b><br>
For the addresses on the back, I needed something light and easy to read at a small size. Myriad Pro turned out to be the font that
worked best in this application. I also wanted it to look modern and clean, which was part of the decision to use that typeface.<br>
<br>
<b>Color:</b><br>
The red trim felt like it was warm and exciting, thus making it fun and inviting. The logo I could not touch, so I needed to do what
I could to work with it.<br>
<br>
<b>Shape:</b><br>
Geometric shapes were chosen for this piece. I wanted a more “official” feel to it, so I focused on geometric shapes.<br>
<br>
<b>Emphasis/Flow:</b><br>
Nothing specific and intentional. I want the person who picks up this piece to see the piece they want to first, examine the card to
see what it is about, and then flip it over for more information.<br>
<br>
<b>Other Design Choices:</b><br>
The fish-bone circles were numbered with locations of the artwork and a map to them on the back. They were used consistently so
the connection could be easily made. The bubbles were different colors not only to make it more fun looking but to create contrast
between each other as you are looking for where they point to on the map.<br>
<br>
<h2>Research:</h2>
I researched past fliers and looked at what other Art Hop events did for their handouts. I even looked into what other peers were
doing to see what I could use and improve upon.<br>
<br>
<h2>Development:</h2>
No artwork was supplied since this was a theoretical event. So, I took my own artwork and used it in this design. The map was
created in Illustrator. The layout was all done in InDesign. The artwork is based on print PDF versions of my artwork converted to
a pixel format. The fish bone circle was created in Illustrator, and the logo was pre-supplied. The bubbles were created in InDesign
and are underlaying the map.<br>
<br>
<h2>Execution:</h2>
This would be distributed in the mail. It would have to be printed on card stock and in full color. The layout does not require bleed,
and I intentionally created a white border so it would not need one. There should be enough room on the bottom right for a barcode
to be added and for a place to put a stamp. This mailing would go out to homes within the Kalamazoo area.<br>
