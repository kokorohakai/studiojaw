<h2>Strategy:</h2>
<b>Purpose:</b><br>
Create a packaging for Shoogs Candies.<br>
<br>
<b>Intended Audience:</b><br>
All ages, especially children.<br>
<br>
<b>Intended Emotion:</b><br>
Old Fashion, fun.<br>
<br>
<h2>Concept:</h2>
<b>Actual Emotion:</b><br>
I went the old fashioned route and created a packaging using colonial style elements.<br>
<br>
<b>Line:</b><br>
The trim of the packaging was chosen to be lined in a way that looked colonial.<br>
<br>
<b>Fonts:</b><br>
Zapfino was chosen in certain areas with the chocolate and used moderately to add a bit of sophistication to the product. I assumed
adults would be purchasing this product, and I figured this made it look classier. The Mint Blasters packaging was more “fun”
and “extreme”, so I used Casual as its primary font. For the gummy worms, I figured the audience is even younger children, so I
specially created a font based off of a sans-serif font. I altered it giving it the outlines and bolder, brighter look.<br>
<br>
<b>Color:</b><br>
This was pre-determined by a panel of peers. The colors were chosen to be candy-like.<br>
<br>
<b>Shape:</b><br>
Organic, to show connection with the product.<br>
<br>
<b>Emphasis/Flow:</b><br>
With the chocolates, it was the chocolate itself so you’d know instantly what the product was. Focusing on making them delicious
looking by bathing them in their surrounding chocolate creates flow up to the logo and ends with the view window.<br>
<br>
For the mints, I focused on the name and showing the mint, with the hard edges of its leaves. I figured this spoke to the customer
about what this product was going to be: really strong mints. Then followed the slogan in case the message wasn’t made clear
enough.<br>
<br>
Finally, with the gummy worms, I focused on creating fun-looking, playful gummy worms that make the product seem enjoyable,
followed by the title of the product, and lastly leading one’s eyes to the packaging itself to see the colorful contents.<br>
<br>
<b>Other Design Choices:</b><br>
Specially rendered bubbles were used on the gummy worms to make the product seem more fun. I had to modify this template to
make sure the view window did not expose the edges of the packaging. The gummy worms packaging is a label for a clear plastic
bag with a top cover.<br>
<br>
<h2>Research:</h2>
I went to Meijer and researched various packaging for what these kinds of candies usually look like. Then, I created my own ideas
from that.<br>
<br>
<h2>Development:</h2>
This was pieced together using Photoshop, Illustrator, and InDesign. The logo is my own logo which was hand drawn and then
vectorized.<br>
<br>
<h2>Execution:</h2>
These pieces were shown off and used to hand out candy as a trial run. If these were to be actually produced, they would need to
be full color. The cost of the gummy worms packaging could be higher because of the plastic bag it needs, and I’d imagine the
pricing would be pretty typical in cost. A special die cut would be needed for the chocolate and mints packaging.<br>
