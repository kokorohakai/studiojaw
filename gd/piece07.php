<h2>Strategy:</h2>
<b>Purpose:</b><br>
Create a wine label for a product named 501 Zine.<br>
<br>
<b>Intended Audience:</b><br>
Young adults age 21–30.<br>
<br>
<b>Intended Image/Motif:</b><br>
Hip, trendy, but still traditional-looking wine label.<br>
<br>
<b>Intended Emotion:</b><br>
Sophisticated<br>
<br>
<h2>Concept:</h2>
<b>Actual Emotion:</b><br>
Using a balance of color and type manipulation, I was able to create a feel of sophistication with a hip attitude.<br>
<br>
<b>Fonts:</b><br>
The font I chose was “MS Mincho.” I wanted something with sharp serifs with thick stems and strokes for the feel of
sophistication and stillness. The font was then modified for the logo.<br>
<br>
<b>Color:</b><br>
The white on black color scheme brought the sophisticated feel to the label, whereas the purple and blue are used to create a hip
feel to it.<br>
<br>
<b>Emphasis/Flow:</b><br>
Emphasis was on the logo, specifically the big “0” in the logo, then on the grapes, and to eventually flow one’s eyes out to see the
full 501 logo.<br>
<br>
<b>Other Design Choices:</b><br>
The grapes were added to give you an idea of what the product is before reading the label, but not to be too much or to look
“cartoony” or “gimicky.” The 5 and 1 were modified to look like a vine.<br>
<br>
<h2>Research:</h2>
I went to a few grocery stores and researched what various wine bottles looked like. Then, being in the age group this is marketing
to, I chose a few of my favorites to investigate. From there I saw a common trend of black colors being used and, very subtly,
bright colors.<br>
<br>
<h2>Development:</h2>
The design was created in Illustrator. The Robert Mondavi Winery logo was supplied to me.<br>
