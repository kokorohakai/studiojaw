<h2>Strategy:</h2>
<b>Purpose:</b><br>
Create an Instruction Booklet cover for the game I created, “Before the Final Frontier.”<br>
<br>
<b>Intended Audience:</b><br>
Gamers, teen to adult (13–25).<br>
<br>
<b>Intended Emotion:</b><br>
Dark, wistful, futuristic.<br>
<br>
<h2>Concept:</h2>
<b>Actual Emotion:</b><br>
The emotion was achieved by using dark colors and I attempted to create high contrast to establish that mood even more.<br>
<br>
<b>Fonts:</b><br>
At first I chose a font that was simplistic and clean, but I later decided to go with something more futuristic and sci-fi like, which is
Nasalization. The font face of the logo was Impact and was heavily modified to give a futuristic look.<br>
<br>
<b>Color:</b><br>
I decided the colors based on contrast: dark colors for the environment and bright colors for things that need emphasis. I focused
on blue where I could to create a mood of depression. Unsaturated colors were chosen to give it a dark, post-apocalyptic feeling.<br>
<br>
<b>Shape:</b><br>
Sharp, angled objects were chosen to give both a sci-fi and fantasy feel to it where I could. The scenery of an eerie forest required
organic objects.<br>
<br>
<b>Emphasis/Flow:</b><br>
The first thing one should notice is the girl, named Rachel, and her glowing sword. Then one should notice the logo. Finally one’s
attention should flow around to the dark scenery.<br>
<br>
<b>Other Design Choices:</b><br>
The screen shots are what appear to be computer screens that are fastened to metal shelving units. Then, the translucent screen
with the displayed text was added to make the scene look more futuristic.<br>
<br>
<h2>Research:</h2>
I researched what other video game manuals traditionally have on them, how they usually handle their typography, and how it’s
usually framed into the design.<br>
<br>
<h2>Development:</h2>
The world was then designed and illustrated in Adobe Illustrator using the same ideas of color and mood. I used a legato rhythm to
establish a depressing calm in the picture.
The artwork was all brought together for color correction and alteration in Photoshop. The shadows and darkness were added in
using the burn tool. I carefully went around the images and gave the images edge and dynamic lighting. Then, the typography,
game logo, and other logos where placed over the artwork in InDesign.<br>
<br>
<h2>Execution:</h2>
I feel this would function well as an instruction booklet for this game if I had ever finished it. It meets the requirements of
promoting the product, intriguing the viewer to want to know more about the game, and highlighting the stylistic choices made for
the game.<br>
