<h2>Introduction:</h2>
I chose to add this poster to my portfolio because of its personal touch. I identify myself as “ota-punk,” in short. Because I do a lot
of coding, both personally and professionally, I felt this poster was a necessity to make. The background, in it’s transitional style,
was chosen for the punk motif. The color was chosen to create a mood of professionalism. My choice of typeface was obvious for
this reason. The placement of the type forms were chosen to give it a symmetric and solid look. Overall, I wanted a “professional
yet rebellious” look and feel. The highlight was chosen to make the connection between the typeface, computers, and computer
work. I keep a copy of this poster in my cubical at work. I feel it helps give people an idea of how my mind works.<br>
<br>
<h2>Strategy:</h2>
<b>Purpose:</b><br>
Create a poster that focuses on the letterform of the Lucida Console font.<br>
<br>
<b>Intended Audience:</b><br>
Coders.<br>
<br>
<b>Intended Emotion:</b><br>
Nostalgia and edginess.<br>
<br>
<h2>Concept:</h2>
<b>Actual Emotion:</b><br>
As mentioned, the style chosen for this poster was transitionalism, which created the edgy emotion. Then the logo was created
using blue to give a trusting feel to it for nostalgic purposes, with a little effect added to created the same nostalgia of using an
older text editor.<br>
<br>
<b>Fonts:</b><br>
Lucida Console.<br>
<br>
<b>Color:</b><br>
Blue to create nostalgia, black to balance it, and grays to create texture.<br>
<br>
<b>Shape:</b><br>
Lucida Console is a very boxy font, so it creates natural geometric shapes. This mono spaced font further creates a certain
geometry that feels technical and edgy.<br>
<br>
<b>Emphasis/Flow:</b><br>
First one starts with the logo, then the slogan text, and then one moves around to the various letter forms which frame the piece.<br>
<br>
<b>Symmetry:</b><br>
Even though I have a transitional style going, I chose to make the poster symmetric to emphasize the stability and reliability of the
font.<br>
<br>
<b>Other Design Choices:</b><br>
The code used in the background is my actual original code! It was extracted from my old portfolio web-site, “Fried Purple
Chicken.”<br>
<br>
<h2>Development:</h2>
This poster was designed in Illustrator.<br>
<br>
<h2>Execution:</h2>
I personally hang this poster in my cubicle at work, and I will be allowing people to download this from my web-site to print for
their own purposes. This poster generally receives positive remarks from peers.<br>
