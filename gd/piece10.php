<h2>Introduction:</h2>
This project has two objectives, Objective A (Obj. A) is the design of the table tent itself. Object B (Obj. B) is the design of a
WithDint advertisement using the table tent design. The table tent was originally designed for the Disability Network, but in the
end was not used for that project. However, because of it’s diversity, it was used for other projects.<br>
<br>
<h2>Strategy:</h2>
<b>Purpose:</b><br>
Obj. A: To create a functioning table tent design that could be produced cheaply and easily which could look like a tent.
Obj. B: Use that design for promoting the web-site news.withdint.com.<br>
<br>
<b>Intended Audience:</b><br>
Obj. A: Anyone who would need a table tent for promotional purposes.<br>
Obj. B: Adults of many ages. Since the young adult audience is more likely to use the internet, the focus is on adults age 18–35.<br>
<br>
<b>Intended Emotion</b><br>
Obj. B: Fun and reliable.<br>
<br>
<h2>Concept (Obj. B):</h2>
<b>Actual Emotion:</b><br>
The emotion of fun was achieved by using our mascot, “Dint-tan,” for the design. The feeling of reliability comes from the
comparison to Tokyo Tower.<br>
<br>
<b>Fonts:</b><br>
WithDint has a standard font that works for our feeling of reliability, Franklin Gothic Medium” Usually all of our promotional
materials use this font. However, I decided to use Helvetica for an even more reliable touch.<br>
<br>
<b>Color:</b><br>
We use the black trim and framing to give the design sophistication, which gives the feeling of reliability.<br>
<br>
<b>Emphasis/Flow:</b><br>
Dint-tan, the tower in the background, and finally the text leading one to the site.<br>
<br>
<h2>Research (Obj. B):</h2>
Like my Terra poster, Dint-tan was drawn from various references and How to Draw Manga books. I did research on Tokyo
Tower, what it is, and what its primary purpose is. From there it was a matter of creating a coherent connection.<br>
<br>
<h2>Development (Obj. A):</h2>
From experience with origami, I started with many different folding ideas. I tried various things until I could figure out what
would create the unique pyramid shape that allows for the design. After finding a way to fold it, I needed to make it simplified. I
worked with different ideas to come to that simplified design. After I figured out the folding, I need to do the math to determine the
maximum area on the face of the design. After looking into the math to get the most area available, I came to the conclusion that
the end angle of the corners needed to be 60 degrees. I then worked the template to be as close to 60 degrees as possible.<br>
<br>
<h2>Development (Obj. B):</h2>
Dint-tan was hand drawn. I designed her originally before I did any illustrator work. I pixel-traced her in Photoshop and applied
line filters to create an anti-alias image. However, for this application, I needed to bring her up to date, so I reworked her design in
Illustrator. The photo comes from a trip I took to Japan which was taken with a Nikon DSLR camera. After touching up Dint-tan
and the photo, I brought it all together using InDesign.<br>
<br>
<h2>Execution (Obj. A):</h2>
This was originally designed to be used for the Disability Network, but they went with a much taller and more expensive design
instead. However, with Eaton Corporation, I was designing a product call the “Product Support Pages.” To remind the managers to
update their pages, we created a design to place on the little pyramid tents. They were the perfect size to fit on their desks without
being obtrusive.<br>
<br>
<h2>Execution (Obj. B):</h2>
Though the design was meant for tables, it turns out these things are best suited for desks. I have a lot of these WithDint table
tents here and there—on my studio Desk and on my desk at work for example. Even a few of my coworkers have ones of these.
Additionally, I also use this to advertise. It’s convenient to leave in public spaces, usually campuses. It can be pre-folded and easily
left behind at a table.<br>
