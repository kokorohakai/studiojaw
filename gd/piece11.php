<h2>Strategy:</h2>
<b>Purpose:</b><br>
Made for the Democratic Party to promote Stem Cell Research through proper education in an entertaining way. The character Stemy was
chosen to be a trustworthy and entertaining way to educate.<br>
<br>
<b>Audience:</b><br>
Any voter and family voting in the next election. The cartoon character appeals to children of the family to educate them, and the hard
facts appeal to the adults of the family. Bringing the two together begins the conversation of properly understanding stem cell research and
what it is.<br>
<br>
<h2>Concept:</h2>
<b>Mood:</b><br>
Trust, with a bit of light-hearted, fun feel.<br>
<br>
<b>Pamphlet Color:</b><br>
Blue for trust and for being the same color that represents the Democratic Party.<br>
<br>
<b>Stemy Color:</b><br>
Yellow/orange was chosen for the skin color of Stemy to harmonize with the blue.<br>
<br>
<b>Rhythm:</b><br>
Legato lines to show calmness.<br>
<br>
<b>Shape:</b><br>
Organic shapes to show trust and natural feelings.<br>
<br>
<b>Emphasis:</b><br>
Stemy himself becomes the emphasis of each page and then leads you to text and info about stem cell research.<br>
<br>
<h2>Research:</h2>
I had determined that the Democratic Party is for stem cell research and had recently altered their platform on embryonic stem cell
research. They still are a proponent of stem cell research, as long as the stem cells don’t come from aborted fetuses.<br>
<br>
<h2>Development:</h2>
Stemy has gone through various stages of design, the most recent of which was all done in Illustrator. A lot needed to be done to make him
more appealing. After attempt upon attempt, I finally came to a design that resonated with a lot of people. The orange color of his stem
cell body harmonizes against the blue pamphlet.<br>
<br>
<h2>Execution:</h2>
I feel this will greatly benefit the Democratic Party in promoting stem cell research through education, and overall I am satisfied with this
piece.<br>
