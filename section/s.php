<h1>Software</h1><br><br>
Most of the software I have developed had been while working for a company. At Diamond Phoenix I wrote "RFM Pick" which allowed
wearhouse users to find a product in their wearhouse and use an RFM scanner to pick the product out of the system. At Premier
Products I wrote a few custom applications that were small but were created to aid the Asterisk PBX system. There were
also a few smaller contracts through Seijinohki as well.<br>
<br>
I have a very strong interest in OpenGL. This is possibly because of my interest in video games. I used to present
a tutorial for the Computer Club of Western Michigan University for OpenGL. It covered the basics of how to get Open GL
up and going and then I demonstrated how to render a cube and texture it. I did this for a few years before moving on.<br>
