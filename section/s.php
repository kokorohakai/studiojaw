			<h2>DigiBand</h2>
			The only game that I have developed that was published. If you know the game "Rock Band", then you will understand
			what this game is. If you know what "StepMania" is, then you will really know what this game is. This game was 
			designed to take fan made files and music, and allow users to create and play their own music with many different
			kinds of musical instruments and controllers that look like musical instruments. This was created before Rock Band, 
			and was intended to look and feel like Konami's b-mani series of games. You could also say it's a free and open 
			source replica of Konami's "Guitar Freaks and Drummania". It's inspiration was the success of "Stepmania" which was
			the equivilent free and open source replica of Konami's "Dance Dance Revolution". This game is globally used, and
			from what I can tell, primarily in South America. Adam Loutzenhiser took on the task of writing the sound engine,
			while I worked on the rest, such as the Graphic Design, Graphic Engine, Game Engine, etc. The game has been halted
			in production for a future version because I do not have the time to work on this by myself. This game can be downloaded 
			and videos of the game can be seen at <a href="http://www.digiband.net/">www.digiband.net</a><br>
