			<h2>RFM Applications Database Editor:</h2>
			The biggest and most complex of all of the applications I created for Eaton. This is essentially a web
			implementation of Microsoft Access, and is built specifically for Microsoft Access databases. It scans for
			access files or allows approved administrators to browse to a file on the web server to open. It gives the 
			user a similar look and feel interface to Microsoft Access. It is heavily reliant on AJAX calls and
			uses a transactional system to ensure data properly saves. It will also inform you when and why data could
			not be saved, and highlights fields that have unsaved changes.<br>
