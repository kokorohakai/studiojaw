I used to work heavily with Japanese animation conventions, and even started one of my own. And one of the things I did was 
a panel / quiz show. At Youmacon 2009, Jafax 2010, and Youmacon 2010, we ran a quiz show style panel that covered the anime
Higurashi no Naku Koro Ni. We would split the audience into two groups, and then the first member of their group to stand 
would get a chance to answer the question. If they anwered correctly, their team would be awarded a point, if wrong the 
other team had a chance to steal the point. The losing team had to play a "Punishment game" as tradition to the anime series. 
Where the winning team would get candy or some other small prize.<br>